
section .data
	msg1 db 10,"The number of positive integers is = "
	msglen1 equ $-msg1
	msg2 db 10,"The number of negative integers is = "
	msglen2 equ $-msg2 
	msg3 db 10,10
	msglen3 equ $-msg3 
	
	arr times 80 db 20h,85h,90h,50h,80h
	pcount db 0
	ncount db 0
		
section .text
	global _start
	
_start:

%macro display 2
	mov eax,4
	mov ebx,1
	mov ecx,%1
	mov edx,%2
	int 80h
%endmacro

mov esi,arr
mov ecx,05h
mov byte[pcount],00h
mov byte[ncount],00h


ll1: mov al,[esi]
and al,80h
jz l1
inc byte[ncount]
jmp l2
l1: inc byte[pcount]
l2: inc esi
loop ll1

display msg1,msglen1
add byte[pcount],30h
display pcount,1
	
display msg2,msglen2
add byte[ncount],30h
display ncount,1

display msg3,msglen3
mov eax,1
int 80h





































































































































































































































































































































































































































































	
